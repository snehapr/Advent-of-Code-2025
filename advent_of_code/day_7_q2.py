def read_grid(filename):
    with open(filename, 'r') as f:
        return [list(line.rstrip('\n')) for line in f if line.strip()]

def find_start(grid):
    for r, row in enumerate(grid):
        for c, val in enumerate(row):
            if val == 'S':
                return r, c
    raise ValueError("No start position 'S' found.")

