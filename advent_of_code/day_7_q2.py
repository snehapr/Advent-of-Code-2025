def count_timelines(grid, r, c, memo):
    rows, cols = len(grid), len(grid[0])
    if not (0 <= r < rows and 0 <= c < cols):
        return 0
    if r == rows - 1:
        return 1
    if (r, c) in memo:
        return memo[(r, c)]
    cell = grid[r][c]
    if cell == '^':
        left = count_timelines(grid, r + 1, c - 1, memo)
        right = count_timelines(grid, r + 1, c + 1, memo)
        memo[(r, c)] = left + right
    else:
        memo[(r, c)] = count_timelines(grid, r + 1, c, memo)
    return memo[(r, c)]

